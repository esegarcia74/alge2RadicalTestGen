class SectionCounts(BaseModel):
    """
    Override the number of questions per section.
    Leave a field as null to use that section's default.
    """
    section_1: Optional[int] = Field(
        default=None, ge=1, le=30,
        description="Questions for Section 1 — Divide & Simplify (default 8)",
        examples=[8],
    )
    section_2: Optional[int] = Field(
        default=None, ge=1, le=20,
        description="Questions for Section 2 — Powers / Exponents (default 4)",
        examples=[4],
    )
    section_3: Optional[int] = Field(
        default=None, ge=1, le=20,
        description="Questions for Section 3 — Multiply Radicals (default 3)",
        examples=[3],
    )


class GenerateRequest(BaseModel):
    """
    All fields have sensible defaults — POST `{}` for a quick test worksheet.
    """
    test_title: str = Field(
        default="Algebra 2 Test: Simplifying Radicals",
        min_length=1, max_length=120,
        description="Title printed at the top of every version.",
        examples=["Period 3 — Radicals Quiz"],
    )
    num_versions: int = Field(
        default=5, ge=1, le=100,
        description="Number of unique randomized versions to generate.",
        examples=[33],
    )
    sections: List[int] = Field(
        default=[1, 2, 3],
        description="Sections to include — any non-empty subset of [1, 2, 3].",
        examples=[[1, 2, 3]],
    )
    section_counts: Optional[SectionCounts] = Field(
        default=None,
        description="Per-section question count overrides. Omit to use defaults.",
    )
    var_min: int = Field(
        default=1, ge=1, le=20,
        description="Minimum variable exponent (controls x^n, y^n, …).",
        examples=[1],
    )
    var_max: int = Field(
        default=10, ge=1, le=20,
        description="Maximum variable exponent.",
        examples=[10],
    )
    root_min: int = Field(
        default=2, ge=2, le=10,
        description="Minimum radical index  (2=√, 3=∛, 4=∜, …).",
        examples=[2],
    )
    root_max: int = Field(
        default=3, ge=2, le=10,
        description="Maximum radical index. Each problem picks randomly in [root_min, root_max].",
        examples=[3],
    )
    name_label: str = Field(
        default="Name", max_length=30,
        description="Label for the student name blank.",
        examples=["Nombre"],
    )
    date_label: str = Field(
        default="Date", max_length=30,
        description="Label for the date blank.",
        examples=["Fecha"],
    )
    period_label: str = Field(
        default="Period", max_length=30,
        description="Label for the period/class blank.",
        examples=["Periodo"],
    )

    @field_validator("sections")
    @classmethod
    def _validate_sections(cls, v):
        bad = [s for s in v if s not in {1, 2, 3}]
        if bad:
            raise ValueError(f"Invalid section number(s): {bad}. Must be 1, 2, or 3.")
        if not v:
            raise ValueError("At least one section must be selected.")
        return sorted(set(v))

    @model_validator(mode="after")
    def _validate_ranges(self):
        if self.var_min > self.var_max:
            raise ValueError("var_min must be ≤ var_max")
        if self.root_min > self.root_max:
            raise ValueError("root_min must be ≤ root_max")
        return self

    def resolved_counts(self) -> Dict[int, int]:
        """Merge user overrides with section defaults."""
        defaults = core.SECTION_Q_DEFAULTS
        if self.section_counts is None:
            return {s: defaults[s] for s in self.sections}
        overrides = {
            1: self.section_counts.section_1,
            2: self.section_counts.section_2,
            3: self.section_counts.section_3,
        }
        return {s: (overrides[s] if overrides[s] is not None else defaults[s])
                for s in self.sections}


