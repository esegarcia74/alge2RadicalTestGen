class GenerateRequest(BaseModel):
    """
    Parameters for worksheet generation.
    All fields have sensible defaults so you can POST `{}` for a quick test.
    """
    test_title: str = Field(
        default="Algebra 2 Test: Simplifying Radicals",
        min_length=1,
        max_length=120,
        description="Title printed at the top of every version.",
        examples=["Period 3 — Radicals Quiz"],
    )
    num_versions: int = Field(
        default=5,
        ge=1,
        le=100,
        description="Number of unique randomized versions to generate.",
        examples=[33],
    )
    sections: List[int] = Field(
        default=[1, 2, 3],
        description="Which sections to include. Any non-empty subset of [1, 2, 3].",
        examples=[[1, 2, 3]],
    )
    section_counts: Optional[SectionConfig] = Field(
        default=None,
        description=(
            "Override the number of questions per section. "
            "Omit a section key to use its default."
        ),
    )
    var_min: int = Field(
        default=1, ge=1, le=20,
        description="Minimum variable exponent (e.g. x^n — controls n).",
        examples=[1],
    )
    var_max: int = Field(
        default=10, ge=1, le=20,
        description="Maximum variable exponent.",
        examples=[10],
    )
    root_min: int = Field(
        default=2, ge=2, le=10,
        description="Minimum radical index (2=sqrt, 3=cbrt, 4=4th-root, …).",
        examples=[2],
    )
    root_max: int = Field(
        default=3, ge=2, le=10,
        description="Maximum radical index. Each problem draws randomly from [root_min, root_max].",
        examples=[3],
    )
    name_label: str = Field(
        default="Name",
        max_length=30,
        description="Label for the student name field.",
        examples=["Nombre"],
    )
    date_label: str = Field(
        default="Date",
        max_length=30,
        description="Label for the date field.",
        examples=["Fecha"],
    )
    period_label: str = Field(
        default="Period",
        max_length=30,
        description="Label for the period field.",
        examples=["Periodo"],
    )

    # ── Validators ────────────────────────────────────────────

    @field_validator("sections")
    @classmethod
    def validate_sections(cls, v):
        valid = {1, 2, 3}
        bad   = [s for s in v if s not in valid]
        if bad:
            raise ValueError(f"Invalid section number(s): {bad}. Must be 1, 2, or 3.")
        if not v:
            raise ValueError("At least one section must be selected.")
        return sorted(set(v))   # deduplicate and sort

    @model_validator(mode="after")
    def validate_ranges(self):
        if self.var_min > self.var_max:
            raise ValueError("var_min must be <= var_max")
        if self.root_min > self.root_max:
            raise ValueError("root_min must be <= root_max")
        return self

    # ── Helper: convert SectionConfig -> Dict[int, int] ──────

    def resolved_section_counts(self) -> Dict[int, int]:
        """Return {sec_num: count} using defaults for any omitted section."""
        defaults = core.SECTION_Q_DEFAULTS
        if self.section_counts is None:
            return {s: defaults[s] for s in self.sections}
        mapping = {
            1: self.section_counts.section_1,
            2: self.section_counts.section_2,
            3: self.section_counts.section_3,
        }
        return {s: (mapping[s] if mapping[s] is not None else defaults[s])
                for s in self.sections}


