@app.post(
    "/api/generate/txt",
    tags=["Generate"],
    summary="Download worksheet as TXT (Google Docs / Auto-LaTeX)",
    responses={200: {"content": {"text/plain": {}}}},
)
def generate_txt(req: GenerateRequest):
    """
    Plain-text worksheet with all math in `$$ ... $$` blocks.

    To render equations in Google Docs:
    1. Paste the file into a Google Doc.
    2. **Extensions → Add-ons → Auto-LaTeX Equations → Start**
    """
    try:
        data = core.build_txt_bytes(**_kwargs(req))
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc

    fname = f"{_safe_name(req.test_title)}_v{req.num_versions}.txt"
    return StreamingResponse(
        io.BytesIO(data),
        media_type="text/plain; charset=utf-8",
        headers={"Content-Disposition": f'attachment; filename="{fname}"'},
    )


@app.post(
    "/api/generate/pdf",
    tags=["Generate"],
    summary="Download print-ready PDF",
    responses={200: {"content": {"application/pdf": {}}}},
)
def generate_pdf(req: GenerateRequest):
    """
    Print-ready PDF worksheet.

    **Layout:**
    - Problems are **left-justified**: question number and prompt on one line,
      math image flush to the left margin — no indentation.
    - A **page break** separates every version's problems from its answer key.
    - The answer key uses a **compact 4-column grid** to fit on one page.

    ⚠️ Slower than TXT — each math expression is rendered as a PNG image.
    Allow 15–60 seconds for 33 versions.
    """
    try:
        data = core.build_pdf_bytes(**_kwargs(req))
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc

    fname = f"{_safe_name(req.test_title)}_v{req.num_versions}.pdf"
    return StreamingResponse(
        io.BytesIO(data),
        media_type="application/pdf",
        headers={"Content-Disposition": f'attachment; filename="{fname}"'},
    )


@app.post(
    "/api/generate/zip",
    tags=["Generate"],
    summary="Download ZIP containing both TXT and PDF",
    responses={200: {"content": {"application/zip": {}}}},
)
def generate_zip(req: GenerateRequest):
    """
    Generates both formats and bundles them into a single ZIP archive.

    Contents:
    - `<title>_v<N>.txt` — Google Docs / Auto-LaTeX version
    - `<title>_v<N>.pdf` — print-ready PDF version
    """
    kw   = _kwargs(req)
    base = f"{_safe_name(req.test_title)}_v{req.num_versions}"
    try:
        txt_data = core.build_txt_bytes(**kw)
        pdf_data = core.build_pdf_bytes(**kw)
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc

    zip_buf = io.BytesIO()
    with zipfile.ZipFile(zip_buf, mode="w", compression=zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(f"{base}.txt", txt_data)
        zf.writestr(f"{base}.pdf", pdf_data)
    zip_buf.seek(0)

    return StreamingResponse(
        zip_buf,
        media_type="application/zip",
        headers={"Content-Disposition": f'attachment; filename="{base}.zip"'},
    )


