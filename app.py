@app.get(
    "/api/sections",
    tags=["Metadata"],
    response_model=SectionsResponse,
    summary="List available sections",
    response_description="Metadata for all three worksheet sections",
)
def get_sections():
    """
    Returns title, description, default question count, and min/max bounds
    for each of the three worksheet sections.
    """
    items = []
    for s in [1, 2, 3]:
        lo, hi = core.SECTION_Q_BOUNDS[s]
        items.append(SectionMeta(
            section           = s,
            title             = core.SECTION_TITLES[s],
            description       = core.SECTION_DESCRIPTIONS[s],
            default_questions = core.SECTION_Q_DEFAULTS[s],
            min_questions     = lo,
            max_questions     = hi,
        ))
    return SectionsResponse(sections=items)


