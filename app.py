@app.post(
    "/api/generate/info",
    tags=["Generate"],
    response_model=InfoResponse,
    summary="Preview generation parameters (no file produced)",
    response_description="Summary of what would be generated",
)
def generate_info(req: GenerateRequest):
    """
    Validates the request and returns a JSON summary of what would be generated —
    useful for confirming settings before requesting a full TXT/PDF download.
    No worksheet is actually produced.
    """
    counts = req.resolved_section_counts()
    total  = sum(counts.values())
    # Very rough TXT size estimate: ~300 chars per question * versions
    est_kb = round(total * req.num_versions * 300 / 1024, 1)

    return InfoResponse(
        test_title                    = req.test_title,
        num_versions                  = req.num_versions,
        sections                      = req.sections,
        section_counts                = {str(k): v for k, v in counts.items()},
        total_questions_per_version   = total,
        var_min                       = req.var_min,
        var_max                       = req.var_max,
        root_min                      = req.root_min,
        root_max                      = req.root_max,
        name_label                    = req.name_label,
        date_label                    = req.date_label,
        period_label                  = req.period_label,
        txt_size_estimate_kb          = est_kb,
        note = (
            "POST to /api/generate/txt or /api/generate/pdf to download the worksheet. "
            "PDF generation is slower due to math image rendering."
        ),
    )


# ── TXT download ──────────────────────────────────────────────

@app.post(
    "/api/generate/txt",
    tags=["Generate"],
    summary="Generate and download worksheet as TXT",
    response_description="UTF-8 text file with $$ ... $$ LaTeX blocks",
    responses={200: {"content": {"text/plain": {}}}},
)
def generate_txt(req: GenerateRequest):
    """
    Generate the worksheet as a plain-text file.

    All math expressions are wrapped in `$$ ... $$`.
    To render them:

    1. Copy the file contents into a Google Doc.
    2. **Extensions → Add-ons → Auto-LaTeX Equations → Start**
    """
    try:
        txt_bytes = core.build_txt_bytes(**_build_kwargs(req))
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc

    fname = f"{_safe_filename(req.test_title)}_v{req.num_versions}.txt"
    return StreamingResponse(
        io.BytesIO(txt_bytes),
        media_type="text/plain; charset=utf-8",
        headers={"Content-Disposition": f'attachment; filename="{fname}"'},
    )


# ── PDF download ──────────────────────────────────────────────

@app.post(
    "/api/generate/pdf",
    tags=["Generate"],
    summary="Generate and download worksheet as PDF",
    response_description="Print-ready PDF with rendered math images",
    responses={200: {"content": {"application/pdf": {}}}},
)
def generate_pdf(req: GenerateRequest):
    """
    Generate the worksheet as a print-ready PDF.

    All math expressions are rendered as crisp images using matplotlib's
    mathtext engine — **no LaTeX installation required on the server**.

    ⚠️ This endpoint is slower than `/api/generate/txt` because each
    mathematical expression is individually rendered as a PNG image.
    For 33 versions with default settings, expect 15–60 seconds.
    """
    try:
        pdf_bytes = core.build_pdf_bytes(**_build_kwargs(req))
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc

    fname = f"{_safe_filename(req.test_title)}_v{req.num_versions}.pdf"
    return StreamingResponse(
        io.BytesIO(pdf_bytes),
        media_type="application/pdf",
        headers={"Content-Disposition": f'attachment; filename="{fname}"'},
    )


# ── ZIP download (TXT + PDF) ──────────────────────────────────

@app.post(
    "/api/generate/zip",
    tags=["Generate"],
    summary="Generate and download both TXT and PDF in a ZIP archive",
    response_description="ZIP file containing the TXT and PDF worksheets",
    responses={200: {"content": {"application/zip": {}}}},
)
def generate_zip(req: GenerateRequest):
    """
    Generate both formats and bundle them in a single ZIP archive.

    The ZIP contains:
    - `<title>_v<N>.txt`  — Google Docs / Auto-LaTeX version
    - `<title>_v<N>.pdf`  — print-ready PDF version
    """
    kwargs = _build_kwargs(req)
    base   = f"{_safe_filename(req.test_title)}_v{req.num_versions}"

    try:
        txt_bytes = core.build_txt_bytes(**kwargs)
        pdf_bytes = core.build_pdf_bytes(**kwargs)
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc

    zip_buf = io.BytesIO()
    with zipfile.ZipFile(zip_buf, mode="w", compression=zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(f"{base}.txt", txt_bytes)
        zf.writestr(f"{base}.pdf", pdf_bytes)
    zip_buf.seek(0)

    return StreamingResponse(
        zip_buf,
        media_type="application/zip",
        headers={"Content-Disposition": f'attachment; filename="{base}.zip"'},
    )


