def _build_kwargs(req: GenerateRequest) -> dict:
    return dict(
        num_versions   = req.num_versions,
        test_title     = req.test_title,
        sections       = req.sections,
        section_counts = req.resolved_section_counts(),
        var_min        = req.var_min,
        var_max        = req.var_max,
        root_min       = req.root_min,
        root_max       = req.root_max,
        name_label     = req.name_label,
        date_label     = req.date_label,
        period_label   = req.period_label,
    )


def _safe_filename(title: str) -> str:
    """Strip characters that are unsafe in Content-Disposition headers."""
    return (title.replace(" ", "_")
                 .replace(":", "")
                 .replace("/", "-")
                 .replace("\\", "-")[:60])


